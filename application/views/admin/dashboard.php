						<h5>Apa Itu Klinik?</h5>
                        <p>Klinik adalah fasilitas pelayanan kesehatan yang menyelenggarakan pelayanan kesehatan perorangan yang menyediakan pelayanan medis dasar dan spesialistik, diselenggarakan oleh lebih dari satu jenis tenaga kesehatan (perawat dan atau bidan) dan dipimpin oleh seorang tenaga medis (dokter, dokter spesialis, dokter gigi atau dokter gigi spesialis).</p>
                        <p><strong>Berdasarkan jenis pelayanannya, klinik dibagi menjadi Klinik Pratama dan Klinik Utama. Kedua macam klinik ini dapat diselenggarakan oleh pemerintah, pemerintah daerah atau masyarakat.</strong></p>
                        <p><strong>Klinik Pratama</strong> adalah klinik yang menyelenggarakan pelayanan medik dasar. <strong>Klinik Utama</strong> adalah klinik yang menyelenggarakan pelayanan medik spesialistik atau pelayanan medik dasar dan spesialistik. Sifat pelayanan kesehatan yang diselenggarakan bisa berupa&nbsp;<strong>rawat jalan, one day care, rawat inap dan/atau home care.</strong></p>
                        <p>Bangunan klinik paling sedikit terdiri atas:</p>
                        <ol>
                            <li>Ruang pendaftaran/ruang tunggu.</li>
                            <li>Ruang konsultasi dokter.</li>
                            <li>Ruang administrasi.</li>
                            <li>Ruang tindakan.</li>
                            <li>Ruang farmasi.</li>
                            <li>Kamar mandi/wc.</li>
                        </ol>
                        <p>Prasarana klinik meliputi:</p>
                        <ol>
                            <li>Instalasi air.</li>
                            <li>Instalasi listrik.</li>
                            <li>Instalasi sirkulasi udara.</li>
                            <li>Sarana pengelolaan limbah.</li>
                            <li>Pencegahan dan penanggulangan kebakaran.</li>
                            <li>Ambulans, untuk klinik yang menyelenggarakan rawat inap.</li>
                            <li>Sarana lainnya sesuai kebutuhan.</li>
                        </ol>
                        <p>Selain itu juga, klinik harus dilengkapi dengan peralatan medis dan nonmedis yang memadai sesuai dengan jenis pelayanan yang diberikan.</p>
                        <p>Syarat peralatan tersebut adalah :</p>
                        <ol>
                            <li>Memenuhi standar mutu, keamanan, dan keselamatan.</li>
                            <li>Memiliki izin edar.</li>
                            <li>Harus di uji dan di kalibrasi secara berkala oleh Balai Pengamanan Fasilitas Kesehatan dan/atau institusi penguji dan pengkalibrasi yang berwenang.</li>
                        </ol>
                        <p><strong>PIMPINAN KLINIK PRATAMA</strong></p>
                        <ul>
                            <li>Pimpinan Klinik Pratama adalah seorang dokter atau dokter gigi.</li>
                            <li>Tenaga medis pada Klinik Pratama minimal terdiri dari 2 (dua) orang dokter dan/atau dokter gigi.</li>
                        </ul>
                        <p><strong>KLINIK UTAMA</strong></p>
                        <ul>
                            <li>Pimpinan Klinik Utama adalah dokter spesialis atau dokter gigi spesialis yang memiliki kompetensi sesuai dengan jenis kliniknya.</li>
                            <li>Tenaga medis pada Klinik Utama minimal terdiri dari 1 (satu) orang dokter spesialis dari masing-masing spesialisasi sesuai jenis pelayanan yang diberikan.</li>
                            <li>Klinik Utama dapat mempekerjakan dokter dan/atau dokter gigi sebagai tenaga pelaksana pelayanan medis.</li>
                        </ul>
                        <p><strong>SURAT IZIN PRAKTIK</strong></p>
                        <ul>
                            <li>Setiap tenaga medis yang berpraktik di klinik harus mempunyai Surat Tanda Registrasi dan Surat Izin Praktik (SIP) sesuai ketentuan peraturan perundang-undangan.</li>
                            <li>Setiap tenaga kesehatan lain yang bekerja di klinik harus mempunyai Surat Izin sebagai tanda registrasi/Surat Tanda Registrasi dan Surat Izin Kerja (SIK) atau Surat Izin Praktik Apoteker (SIPA) sesuai ketentuan peraturan perundang-undangan.</li>
                        </ul>
                        <p><strong>PERIZINAN</strong></p>
                        <ul>
                            <li>Untuk mendirikan dan menyelenggarakan klinik harus mendapat izin dari pemerintah daerah kabupaten/kota setelah mendapatkan rekomendasi dari dinas kesehatan kabupaten/kota setempat.</li>
                            <li>Dinas kesehatan kabupaten/kota mengeluarkan rekomendasi sebagaimana dimaksud pada ayat (1) setelah klinik memenuhi ketentuan persyaratan klinik dalam Peraturan ini.</li>
                        </ul>
                        <p>Permohonan izin klinik diajukan dengan melampirkan :</p>
                        <ol>
                            <li>Surat rekomendasi dari dinas kesehatan setempat.</li>
                            <li>Salinan/fotokopi pendirian badan usaha kecuali untuk kepemilikan perorangan.</li>
                            <li>Identitas lengkap pemohon.</li>
                            <li>Surat keterangan persetujuan lokasi dari pemerintah daerah setempat.</li>
                            <li>Bukti hak kepemilikan atau penggunaan tanah atau izin penggunaan bangunan untuk penyelenggaraan kegiatan bagi milik pribadi atau surat kontrak minimal selama 5 (lima) tahun bagi yang menyewa bangunan untuk penyelenggaraan kegiatan.</li>
                            <li>Dokumen Upaya Pengelolaan Lingkungan (UKL) dan Upaya Pemantauan Lingkungan (UPL).</li>
                            <li>Profil klinik yang akan didirikan meliputi struktur organisasi kepengurusan, tenaga kesehatan, sarana dan prasarana, dan peralatan serta pelayanan yang diberikan.</li>
                            <li>Persyaratan administrasi lain sesuai dengan ketentuan peraturan perundang-undangan.</li>
                            <li>Izin klinik diberikan untuk jangka waktu 5 (lima) tahun dan dapat diperpanjang dengan mengajukan permohonan perpanjangan 6 (enam) bulan sebelum habis masa berlaku izinnya.</li>
                        </ol>
                        <p><strong>PELAYANAN RAWAT INAP</strong></p>
                        <p>Klinik yang menyelenggarakan pelayanan rawat inap harus menyediakan :</p>
                        <ol>
                            <li>Ruang rawat inap yang memenuhi persyaratan.</li>
                            <li>Tempat tidur pasien minimal 5 (lima) dan maksimal 10 (sepuluh).</li>
                            <li>Tenaga medis dan keperawatan yang sesuai jumlah dan kualifikasinya.</li>
                            <li>Tenaga gizi, tenaga analis kesehatan, tenaga kefarmasian dan tenaga kesehatan dan/atau tenaga non kesehatan lain sesuai kebutuhan.</li>
                            <li>Dapur gizi.</li>
                            <li>Pelayanan laboratorium Klinik Pratama.</li>
                        </ol>
                        <p>Pelayanan rawat inap hanya dapat dilakukan maksimal selama 5 (lima) hari.</p>
                        <p>Berdasarkan <em>Peraturan Menteri Kesehatan ( Permenkes ) No. 28 Tahun 2011</em> salah satu persyaratan izin mendirikan dan menyelenggarakan klinik harus melampirkan Dokumen Upaya Pengelolaan Lingkungan (UKL) dan Upaya Pemantauan Lingkungan (UPL). Karena Klinik merupakan suatu usaha dan/atau kegiatan yang spesifik yang menimbulkan dampak terhadap lingkungan. Setiap Klinik menghasilkan limbah medis dan non medis baik dalam bentuk padat maupun cair. Maka Dinas Kesehatan mengadakan Pertemuan antara Pemilik Sarana Kesehatan Swasta dengan PT. Tenang Jaya Sejahtera untuk melakukan Perjanjian Kerjasama dalam rangka Pembuangan Limbah Medis.</p>
